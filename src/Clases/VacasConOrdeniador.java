public class VacasConOrdeniador {
    
    String alias;
    String estado;

    public VacasConOrdeniador(String alias, String estado) {
        this.alias = alias;
        this.estado = estado;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
    
}
